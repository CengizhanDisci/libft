#include "libft.h"

char	*ft_strchr(const char *s, int c)
/*aranan harfi bulduðunda kelime ve sonrasýndaki
cümleleri return eder*/
{
	char	*ptr;

	ptr = (char *)s;
	while (*ptr)
	{
		if (*ptr == (unsigned char)c)
			return (ptr);
		ptr++;
	}
	if (c == 0)
		return (ptr);
	return (NULL);
}


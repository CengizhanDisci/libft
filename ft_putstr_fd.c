#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
//standart çıktıya bir dizeyi yazar.
{
	int	i;

	i = 0;
	if (s)
		while (s[i] != '\0')
			write(fd, &s[i++], 1);
}
